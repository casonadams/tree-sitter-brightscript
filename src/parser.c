      if (lookahead == '"') ADVANCE(17);
      if (lookahead == '\'') ADVANCE(243);
      if (lookahead == '(') ADVANCE(132);
      if (lookahead == ')') ADVANCE(133);
      if (lookahead == '*') ADVANCE(161);
